#include "libft.h"

int	ft_atoi(const char *str)
{
	long	num;
	int		i;
	int		minus;

	num = 0;
	i = 0;
	minus = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		num = num * 10 + (str[i++] - '0');
	if (minus)
		num *= -1;
	return (num);
}
